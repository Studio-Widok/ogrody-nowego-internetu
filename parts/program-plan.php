      <p>Jak często zadajemy sobie pytanie, na jakich zasadach korzystamy z
        technologii cyfrowych? Czy są to nasze zasady, czy tylko ich pozory, bo
        tak naprawdę, jako konsumenci rozwiązań cyfrowych mamy bardzo niewiele
