        podszyte manipulacją. Wyłaniają się narracje o ograniczeniu poczucia
        sprawczości użytkowników, a oni sami opisują swoje doświadczenia w
        kategoriach inercji. A co jeśli ten krajobraz można transformować?</p>
      <p>W ramach naszego wystąpienia zastanowimy się, czy możliwa jest
        transformacja myślenia o “punktach styku” z technologią w rozbudowane
        "przestrzenie dialogu" i świadomego zaangażowania. Przyglądając się
        zasobom i potencjałowi człowieka, jak również jego ograniczeniom i
        podatności na uwikłanie, zaprosimy do dyskusji dotyczącej możliwości
        wprowadzania własnych zasad do relacji z technologiami cyfrowymi.</p>
